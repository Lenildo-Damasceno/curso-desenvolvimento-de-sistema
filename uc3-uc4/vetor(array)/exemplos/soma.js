// //  Exemplo: Somando todos os valores de um vetor
let numeros = [10, 20, 30, 40, 50];
 let soma = 100;
for (let i = 0; i < numeros.length; i++) {
     soma += numeros[i];
}
console.log("Soma dos valores do vetor:", soma);



